import axios from 'axios';
import { API_URL, MEDIA_API_URL } from '@/config';
import defaultPoster from '@/assets/img/asia.jpg';

export default {
  name: 'AboutSection',
  data() {
    return {
      videoUrl: '',
      posterUrl: '',
      isVideoPlaying: false,
      isVideoLoading: false,
      videoLoaded: false
    };
  },
  computed: {
    posterImage() {
      return this.posterUrl ? MEDIA_API_URL + this.posterUrl : defaultPoster;
    }
  },
  async created() {
    await this.fetchVideoData();
  },
  methods: {
    async fetchVideoData() {
      try {
        console.log('–ó–∞–≥—Ä—É–∂–∞–µ–º –≤–∏–¥–µ–æ –∏–∑:', `${API_URL}/videos/`);
        const response = await axios.get(`${API_URL}/videos/`);
        console.log('–ü–æ–ª—É—á–µ–Ω–Ω—ã–µ –¥–∞–Ω–Ω—ã–µ –≤–∏–¥–µ–æ:', response.data);
        
        if (response.data.results && response.data.results.length > 0) {
          const videoData = response.data.results[0];
          this.videoUrl = MEDIA_API_URL + videoData.video;
          
          // –ï—Å–ª–∏ –µ—Å—Ç—å –ø–æ—Å—Ç–µ—Ä –≤ –æ—Ç–≤–µ—Ç–µ API
          if (videoData.poster) {
            this.posterUrl = videoData.poster;
          }
          
          console.log('Video URL:', this.videoUrl);
          console.log('Poster URL:', this.posterUrl);
        } else {
          console.log('–í–∏–¥–µ–æ –Ω–µ –Ω–∞–π–¥–µ–Ω—ã –≤ –æ—Ç–≤–µ—Ç–µ API');
        }
      } catch (error) {
        console.error('Error fetching video:', error);
      }
    },
    
    setupLazyLoading() {
      const videoElement = this.$refs.videoPlayer;
      
      if (videoElement && 'IntersectionObserver' in window) {
        const observer = new IntersectionObserver((entries) => {
          entries.forEach(entry => {
            if (entry.isIntersecting && !this.videoLoaded) {
              this.preloadVideo();
              observer.disconnect();
            }
          });
        }, {
          rootMargin: '200px',
          threshold: 0.1
        });
        
        observer.observe(videoElement);
      } else {
        this.preloadVideo();
      }
    },
    
    preloadVideo() {
      const video = this.$refs.videoPlayer;
      if (video) {
        video.preload = 'auto';
        this.videoLoaded = true;
      }
    },
    
    async playVideo() {
      const video = this.$refs.videoPlayer;
      
      if (video) {
        this.isVideoLoading = true;
        
        try {
          await video.play();
        } catch (error) {
          console.error('Error playing video:', error);
          this.isVideoLoading = false;
        }
      }
    },
    
    onVideoPlay() {
      this.isVideoPlaying = true;
      this.isVideoLoading = false;
    },
    
    onVideoPause() {
      this.isVideoPlaying = false;
    },
    
    onVideoEnd() {
      this.isVideoPlaying = false;
    }
  },
  
  mounted() {
    this.setupLazyLoading();
  },
  
  beforeUnmount() {
    if (this.$refs.videoPlayer) {
      this.$refs.videoPlayer.pause();
    }
  }
};
